/**
 * Get episode by slug
 */
export const getEpisodeBySlug = (slug) => {
  return podcastEpisodes.find(ep => ep.slug === slug);
};

/**
 * Get episode by ID
 */
export const getEpisodeById = (id) => {
  return podcastEpisodes.find(ep => ep.id === id);
};

/**
 * Get featured episodes
 */
export const getFeaturedEpisodes = () => {
  return podcastEpisodes.filter(ep => ep.featured);
};

/**
 * Get episodes by category
 */
export const getEpisodesByCategory = (category) => {
  return podcastEpisodes.filter(ep => ep.category === category);
};

/**
 * Search episodes by keyword
 */
export const searchEpisodes = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return podcastEpisodes.filter(ep =>
    ep.title.toLowerCase().includes(lowercaseQuery) ||
    ep.description.toLowerCase().includes(lowercaseQuery) ||
    ep.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery)) ||
    ep.transcript.keywords.some(kw => kw.toLowerCase().includes(lowercaseQuery))
  );
};

/**
 * Get all categories
 */
export const getPodcastCategories = () => {
  const categories = [...new Set(podcastEpisodes.map(ep => ep.category))];
  return categories.sort();
};

export default podcastEpisodes;
